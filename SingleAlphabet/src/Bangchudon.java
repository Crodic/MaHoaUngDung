    private void ranDomkeyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ranDomkeyActionPerformed
        // TODO add your handling code here:
        String keyCore="abcdefghijklmnopqrstuvwxyz";//Khởi tạo bảng mã ASCII
        
        Random ranDom=new Random();//Khởi tạo 1 biến để chứa các chữ số random
        String after ="";
        for (int i = 0; i < keyCore.length(); i++) {//sử dụng vòng lặp
            int index =ranDom.nextInt(keyCore.length());//tạo 1 biến là kết quả từ việc 
                                            //lấy những con số ngẫu nhiên ở tại chuỗi string keycore đã khai báo
            char ramChar =keyCore.charAt(index);//trả về chữ có vị trí tương ứng với index
            
            if (after.indexOf(ramChar)==-1)//nếu chữ cái nằm trong khoảng của ramChar sẽ trả về -1
            {
                after+=ramChar;
            }
            else{ // nếu ko tìm tiếp
                i--;
            }
        }
       
        //Xuất ra màn hình
        textField1.setText(after);
        textField2.setText(after);
    }//GEN-LAST:event_ranDomkeyActionPerformed

